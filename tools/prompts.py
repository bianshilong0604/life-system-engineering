"""
个人总体设计部 · LLM Prompt 唯一来源
=====================================
所有 AI 调用的 user 消息模板集中在这里。[[双链]] 约定只在本文件维护一处,
assistant / server / jianwen 三处统一引用,改一处全生效。

约定:
  · 每个函数返回 user 消息字符串(已填好变量)。
  · system 角色统一用 assistant.SYSTEM_ROLE,不在这里重复。
  · 不 import 任何本地模块(纯字符串层,避免循环依赖)。
"""

# ── 周复盘:Verify → Reflect → Patch → Plan ─────────────
def verify_question(ctx):
    """周复盘第 1 步:基于上周 Plan 逐条问做到没。(assistant 交互专用)"""
    return ctx + (
        "\n\n现在开始本周复盘的第一步 Verify。基于我上一份复盘里的 Plan,"
        "把「研究工作/学习成长」上周定的事逐条列出来,问我每条做到没、没做到一句话原因。"
        "只输出问题,简洁。")


def reflect_patch(ctx, verify):
    """周复盘第 2 步:Reflect(能力/系统归类)+ Patch(提炼 0-1 条规则)。(assistant + server 共用)"""
    return ctx + (
        f"\n\n我对 Verify 的回答:\n{verify}\n\n"
        "现在做 Reflect:对没做到的,判断能力问题还是系统问题(系统问题归类)。"
        "再做 Patch:如有系统问题,提炼 0-1 条规则(仿规则库格式)。简洁输出。")


def plan(ctx, verify, reflect_patch_text):
    """周复盘第 3 步:给下周 3 个子系统各定 1-2 件。(assistant + server 共用)"""
    return ctx + (
        f"\n\nVerify:{verify}\nReflect/Patch:{reflect_patch_text}\n\n"
        "现在做 Plan:给下周「研究工作/学习成长/复盘进化」各定 1-2 件,少而精,"
        "能外包给 AI/工具的标注→外包。简洁输出。")


# ── 踩坑 ────────────────────────────────────────────────
def pit(rules_text, pit_text):
    """踩坑:判断一次性/系统问题,提炼规则(末尾带 [[双链]] 链回已有规则/复盘)。"""
    return rules_text + (
        f"\n\n我在研究工作中踩了个坑:\n{pit_text}\n\n"
        "判断:1)一次性问题还是会重复的系统问题 2)系统问题就归类并提炼1条规则(仿上面R格式) "
        "3)如果不值得入库,直说「这条不用记」+原因。别过度反应。"
        "4)若提炼了规则,在其末尾用 [[双链]] 链到相关的已有规则/复盘(如 [[R1]]),便于将来构建知识图谱。")


# ── 沉淀知识 ────────────────────────────────────────────
def learn(src):
    """把输入结构化沉淀进知识库(末尾带「相关:」[[双链]] 行)。src 已是文本,内部截断 8000 字。"""
    return (
        f"把这个输入沉淀进我的学习成长子系统,结构化(不只存原文):\n\n{src[:8000]}\n\n"
        "输出 Markdown:1)一句话核心 2)3-5个关键点(带可复用接口) "
        "3)今日意义(能用到我研究/学习方法的哪点) 4)判断:值得长期留存还是看完就够(防囤积) "
        "5)末尾加一行「相关:」,用 [[双链]] 语法链到可能相关的规则/复盘/其他知识"
        "(如 [[R1]] [[2026-06-15]]),想不到就写「相关:暂无」。这是为将来知识图谱预埋,务必保留这行。")


# ── 月度体检 ────────────────────────────────────────────
def checkup(ctx, revs_text):
    """月度体检:进展/规则库/反复问题/轻重/保持还是扩张。"""
    return ctx + "\n\n最近复盘:\n" + revs_text + (
        "\n\n做月度体检,回答:1)哪个子系统真进展、哪个空转 2)规则库长了几条、有无该合并的 "
        "3)有无反复出现却没被规则解决的问题 4)系统让我更轻松还是更累 "
        "5)建议:保持3个/解封1个新子系统/简化——给理由,别默认劝我扩张。说真话。")


# ── 今日见闻池(jianwen 专用)────────────────────────────
def inspiration(raw):
    """灵感随记:一闪而过 → 能用上的东西(末尾带「相关:」[[双链]] 行)。"""
    return (
        "下面是我随手记下的一个灵感/想法/小思考。请帮我把它从「一闪而过」变成「能用上的东西」。\n\n"
        "我的原文:\n{raw}\n\n"
        "请输出 Markdown,结构如下:\n"
        "1. **一句话提炼**:用一句话点破这个想法的核心。\n"
        "2. **展开思考**:3-4 句,把这个想法讲清楚、补全我没说透的逻辑,必要时指出盲点或反例。\n"
        "3. **可落地的方向**:1-3 条具体能做的事/能验证的实验/能延伸的问题(要具体,不要空话)。\n"
        "4. **价值判断**:这个灵感是「值得长期留存深挖」还是「记一下就够」?给理由,别都说值得。\n"
        "5. 末尾加一行「相关:」,用 [[双链]] 语法链到可能相关的规则/复盘/已有知识(如 [[R1]]),"
        "想不到就写「相关:暂无」。这是为知识图谱预埋,务必保留这行。\n"
        "全程中文,简洁不啰嗦。").format(raw=raw)


def archive(title, src, body):
    """见闻归档进知识库:结构化 + 建关联 + 标今日意义(末尾带「相关:」[[双链]] 行)。"""
    return (
        "把下面这条「今日见闻」沉淀进我的学习成长知识库。不要照抄,要抽结构、建关联、标今日意义。\n\n"
        "标题:{title}\n来源:{src}\n\n内容:\n{body}\n\n"
        "请输出 Markdown:\n"
        "1) 一句话核心\n"
        "2) 3-5 个关键点(带可复用的接口/方法/结论)\n"
        "3) 今日意义(能用到我研究工作或学习方法的哪一点)\n"
        "4) 末尾加一行「相关:」,用 [[双链]] 语法链到可能相关的规则/复盘/其他知识"
        "(如 [[R1]] [[2026-06-15]]),想不到就写「相关:暂无」。务必保留这行。\n"
        "全程中文。").format(title=title, src=src, body=body)
